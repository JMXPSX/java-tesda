public class Arithmetic {

	
	public static void main(String[] args) {
	
		int a = 10;
		int b = 20;
		int c = 25;
		int d = 25;
		
		System.out.println("Math/Arithmetic Operators");
		System.out.println("1. a + b = " + (a + b));
		System.out.println("2. a - b = " + (a - b));
		System.out.println("3. a * b = " + (a * b));
		System.out.println("4. b / a = " + (b / a));
		System.out.println("5. b mod a = " + (b % a));
		System.out.println("6. c mod a = " + (c % a));
		System.out.println("7. a++ = " + a++);
		System.out.println("8. b++ = " + b++);
		System.out.println("9. ++b = " + ++b);
		System.out.println("10. b-- = " + b--);
		
	}

}
