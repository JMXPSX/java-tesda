public class CRelation {

	
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = 25;
		int d = 25;
		
		System.out.println("Relational Operators");
		System.out.println("a = 10");
		System.out.println("b = 20");
		System.out.println("c = 25");
		System.out.println("d = 25");
		System.out.println();
		System.out.println("1. a == b: " + (a == b));
		System.out.println("2. a != b: " + (a != b));
		System.out.println("3. a > b: " + (a > b));
		System.out.println("4. b < a: " + (b < a));
		System.out.println("5. b >= a: " + (b >= a));
		System.out.println("6. c >= a: " + (c <= a));

	}

}
